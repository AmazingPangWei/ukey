package com.ukey.pojo;

public class School
{
	private int schoolid;
	private String sname;

	public int getSchoolid()
	{
		return schoolid;
	}
	public void setSchoolid(int schoolid)
	{
		this.schoolid = schoolid;
	}
	public String getSname()
	{
		return sname;
	}
	public void setSname(String sname)
	{
		this.sname = sname;
	}
}
